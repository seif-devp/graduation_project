class InterviewEntity {
  final String id;
  final String jobTitle;
  final String company;
  final String date;
  final String time;
  final String type;
  final String meetingLink;
  final String status;

  const InterviewEntity({
    required this.id,
    required this.jobTitle,
    required this.company,
    required this.date,
    required this.time,
    required this.type,
    required this.meetingLink,
    required this.status,
  });
}